                <div class = "faq page-section" id="faq">
                    <h3 class = "animated fadeIn">FAQ</h3>
                    <div class = "row">
                        <div class = "faq-col l">
                            <h4>What happened to hackBCA?</h4>
                            <p>For four years, <a href = "/history">hackBCA</a> has provided inspiration, education, and community to our participants. But after hackBCA IV, our team decided that we wanted to focus on education as our primary mission and we felt that it was time to light a new torch. Over the past year, we’ve been busy transforming our hackathon format into <b>Imagine 2018</b> in order to provide an unforgettable and diverse learning experience to <b>all</b> of our attendees. We know it’s been over a year since the last hackBCA event and we’re so excited for all that we have in store for you guys!</p>
                        </div>
                        <div class = "faq-col r">
                            <h4>What kind of workshops and talks will be offered?</h4>
                            <p>One of our core goals is offer a diverse range of learning opportunities so that there’s something for everyone. To that end, our workshops and talks encompass a wide variety of computer science related fields for all different experience levels, from different programming languages, cybersecurity, algorithms, UI/UX design, entrepreneurship, and much more. <a href = "registration/index.php">Sign up</a> for an account to be notified when we release our full speakerdeck!</p>
                        </div>
                        <div class="clear" style="clear: both;"></div>
                    </div>
                    <div class = "row">
                        <div class = "faq-col l">
                            <h4>Is there going to be cupstacking?</h4>
                            <p>It wouldn’t be a hackBCA event without cupstacking! In addition to workshops and talks, Imagine will still have all of the awesome activities that hackBCA has always had, as well as plenty of opportunities to meet like-minded people and have fun.</p>
                        </div>
                        <div class = "faq-col r">
                            <h4>Who can attend?</h4>
                            <p>Imagine is open to all high school and undergraduate students of all backgrounds and experience levels - just don’t forget to fill out our application. If you don’t fit this category but would still like to attend, reach out to us and consider coming as a mentor or volunteer! NOTE: Imagine will not be overnight this year. However, it is still a two day event.</p>
                        </div>
                        <div class="clear" style="clear: both;"></div>
                    </div>
                    <div class = "row">
                        <div class = "faq-col l">
                            <h4>What if I don't know how to code?</h4>
                            <p>Then you’ve come to the right place! We’re open to students of all levels and all beginners are encouraged to come. Imagine provides an awesome opportunity to dive into programming. Our workshops and mentors will help you get started with the basics of programming and you’ll leave our event having learned a ton.</p>
                        </div>
                        <div class = "faq-col r">
                            <h4>Is this event free?</h4>
                            <p>Imagine will have an entry fee of $30, which will cover all food, swag, and knowledge. However, if you cannot afford the fee, please reach out to us! We’d love to have you at our event and will be offering scholarships.</p>
                        </div>
                        <div class="clear" style="clear: both;"></div>
                    </div>
                    <div class = "row">
                        <div class = "faq-col l">
                            <h4>Can I speak at the event?</h4>
                            <p>If you are a tech professional, professor, or post graduate student, we would love to have you here! Just apply to speak <a href = "speakers/index.php">here</a> and we will get in touch with you shortly.</p>
                        </div>
                        <div class = "faq-col r">
                            <h4>When will applications open?</h4>
                            <p>Applications will open soon! Sign up for an account to be updated.</p>
